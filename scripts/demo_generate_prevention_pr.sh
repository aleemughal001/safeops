find {OUT_DIR} -type f -maxdepth 5 -print
sed -n '1,200p' {OUT_DIR}/PR_BODY.md
```

## Future real PR flow

Milestone 12 should turn this into a real GitHub/GitLab PR:

1. Create branch `{branch}`
2. Add the generated prevention files
3. Commit the guardrail
4. Open PR with the generated body
5. Request engineer approval
""",
)

print("SafeOps prevention PR draft generated.")
print(f"Incident:           {incident_id}")
print(f"Service:            {service}")
print(f"Required env:       {env_name}")
print(f"Suggested branch:   {branch}")
print(f"Output directory:   {OUT_DIR}")
print()
print("Generated files:")
for path in sorted(OUT_DIR.rglob("*")):
    if path.is_file():
        print(f"- {path}")
PY
